package type_casting1;

public class Primitives_casting {
	
	public static void main(String[] args)
	{
		int a=10;
		System.out.println(a);
		double b=a;
		System.out.println(b);     //emplixit casting
		
		double c=140;
		System.out.println(c);       //implisite casting
		byte d=(byte)c;
		System.out.println(d);
		
		
		
			}

}
